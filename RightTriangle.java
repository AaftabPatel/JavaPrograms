package Shapes;
public class RightTriangle{
    public void area(float h, float b){
        System.out.println("Area of Right Angled Triangle is: "+(0.5*b*h));
    }
    public void peri(float h, float b){
        System.out.println("Perimetere of Right Angled Triangle is: "+(b*h));
    }
}
